# 1. Variables
# Variables are used to store data values.

# String
my_string = "Hello World"

# Integer
my_int = 5

# Float
my_float = 5.5

# Boolean
my_bool = True

# List
""" Mutable: Can be changed after they are assigned
    Ordered: Items have a defined order, and that order will not change.
    Allows duplicate members.
    Allow different data types.
"""
my_list = [1, 2, 3, 4, 5, 6]

# Dictionary
"""Dictionary is a collection which is unordered, changeable and indexed."""
my_dict = {"name": "John", "age": 36}

print(my_dict["name"])

# Tuple
# Immutable: Cant be changed after they are assigned
# Ordered: Items have a defined order, and that order will not change.
# Allows duplicate members.
# Allows different data types.
my_tuple = (1, 2, 3)

# Constants do not exist in Python,
# but you can use all capital letters to indicate a constant variable.
MY_CONSTANT = 5